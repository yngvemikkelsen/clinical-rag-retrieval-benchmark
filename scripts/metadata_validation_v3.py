def load_mtsamples(n=100, seed=42):
    from datasets import load_dataset
    print("Loading MTSamples...")
    try:
        ds = load_dataset("mteb/mtsamples", split="train", token=HF_TOKEN or None)
        df = ds.to_pandas()
    except Exception:
        try:
            ds = load_dataset("lewtun/mtsamples", split="train", token=HF_TOKEN or None)
            df = ds.to_pandas()
        except Exception:
            for p in [DATA_DIR / "mtsamples.csv", Path("mtsamples.csv"), Path("/content/mtsamples.csv")]:
                if p.exists():
                    df = pd.read_csv(p)
                    break
            else:
                raise FileNotFoundError("MTSamples not found. Upload mtsamples.csv to /content/sample_data/")
    col_map = {}
    for c in df.columns:
        cl = c.lower().strip()
        if "transcription" in cl: col_map[c] = "text"
        elif cl == "text" and "text" not in col_map.values(): col_map[c] = "text"
        elif "specialty" in cl or "medical_specialty" in cl: col_map[c] = "specialty"
        elif "description" in cl: col_map[c] = "description"
        elif "keywords" in cl: col_map[c] = "keywords"
        elif "sample_name" in cl or "title" in cl: col_map[c] = "title"
    df = df.rename(columns=col_map)
    if "text" not in df.columns:
        for c in df.columns:
            if df[c].dtype == object and df[c].str.len().median() > 200:
                df = df.rename(columns={c: "text"})
                break
    df = df.dropna(subset=["text"])
    df = df[df["text"].str.len() > 100].reset_index(drop=True)
    if len(df) > n:
        df = df.sample(n=n, random_state=seed).reset_index(drop=True)
    print(f"  Loaded {len(df)} notes")
    return df


