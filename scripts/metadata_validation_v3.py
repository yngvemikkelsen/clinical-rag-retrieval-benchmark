def call_gpt4o(prompt, temperature=0.0, max_tokens=300):
    for attempt in range(3):
        try:
            r = client.chat.completions.create(
                model="gpt-4o", temperature=temperature, max_tokens=max_tokens,
                messages=[{"role": "user", "content": prompt}])
            return r.choices[0].message.content.strip()
        except Exception as e:
            if attempt < 2:
                time.sleep(2 ** attempt)
            else:
                print(f"    GPT-4o error: {e}")
                return None


def extract_metadata(text):
    raw = call_gpt4o(META_PROMPT.format(text=text[:3000]))
    if raw is None:
        return {"specialty": "Unknown", "note_type": "Clinical Note",
                "primary_diagnosis": "Unknown", "secondary_diagnoses": [], "patient_demographics": "adult"}
    raw = re.sub(r'^```(?:json)?\s*', '', raw)
    raw = re.sub(r'\s*```$', '', raw)
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"specialty": "Unknown", "note_type": "Clinical Note",
                "primary_diagnosis": "Unknown", "secondary_diagnoses": [], "patient_demographics": "adult"}


def gen_metadata_query(meta, template):
    sec = ", ".join(meta.get("secondary_diagnoses", []) or []) or "none"
    prompt = template.format(
        specialty=meta.get("specialty", "Unknown"),
        note_type=meta.get("note_type", "Clinical Note"),
        primary_diagnosis=meta.get("primary_diagnosis", "Unknown"),
        secondary=sec,
        demographics=meta.get("patient_demographics", "adult"))
    result = call_gpt4o(prompt, temperature=0.3, max_tokens=150)
    return result or meta.get("primary_diagnosis", "clinical query")


