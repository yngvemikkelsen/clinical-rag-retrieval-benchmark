def compute_retrieval_metrics(q_emb, d_emb, k_values=[1, 5, 10]):
    sim_matrix = q_emb @ d_emb.T
    n = sim_matrix.shape[0]
    ranks = []
    for i in range(n):
        scores = sim_matrix[i]
        rank = (scores > scores[i]).sum() + 1
        ranks.append(rank)
    ranks = np.array(ranks)
    metrics = {}
    for k in k_values:
        rr = np.where(ranks <= k, 1.0 / ranks, 0.0)
        metrics[f"MRR@{k}"] = float(rr.mean())
    for k in k_values:
        metrics[f"Recall@{k}"] = float((ranks <= k).mean())
    metrics["P@1"] = float((ranks == 1).mean())
    return metrics, ranks


def bootstrap_ci(ranks, metric_fn, n_bootstrap=1000, seed=42):
    rng = np.random.RandomState(seed)
    n = len(ranks)
    boot_values = [metric_fn(ranks[rng.choice(n, size=n, replace=True)]) for _ in range(n_bootstrap)]
    return float(np.percentile(boot_values, 2.5)), float(np.percentile(boot_values, 97.5))


def mrr_at_10(ranks):
    return np.where(ranks <= 10, 1.0 / ranks, 0.0).mean()


