def generate_synthetic_notes(n=100, seed=42):
    from transformers import AutoModelForCausalLM, AutoTokenizer
    # Check for cache from the main benchmark run
    for cache in [OUTPUT_DIR / "synthetic_notes.csv",
                  Path("./benchmark_v3_outputs/synthetic_notes.csv"),
                  DATA_DIR / "synthetic_notes.csv"]:
        if cache.exists():
            print(f"Loading cached synthetic notes from {cache}")
            df = pd.read_csv(cache)
            if len(df) >= n:
                return df.sample(n=n, random_state=seed).reset_index(drop=True)

    print(f"Generating {n} synthetic notes with {SYNTH_MODEL}...")
    tokenizer = AutoTokenizer.from_pretrained(SYNTH_MODEL, token=HF_TOKEN or None)
    model = AutoModelForCausalLM.from_pretrained(
        SYNTH_MODEL, torch_dtype=torch.float16, device_map="auto", token=HF_TOKEN or None)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    rng = np.random.RandomState(seed)
    notes_per_spec = n // len(SPECIALTIES_FOR_SYNTH)
    remainder = n % len(SPECIALTIES_FOR_SYNTH)
    all_notes = []

    for i, specialty in enumerate(tqdm(SPECIALTIES_FOR_SYNTH, desc="Specialties")):
        count = notes_per_spec + (1 if i < remainder else 0)
        for j in range(count):
            prompt = SYNTH_PROMPT_TEMPLATE.format(specialty=specialty)
            inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
            with torch.no_grad():
                outputs = model.generate(**inputs, max_new_tokens=600, temperature=0.8,
                                          top_p=0.9, do_sample=True, pad_token_id=tokenizer.eos_token_id)
            full_text = tokenizer.decode(outputs[0], skip_special_tokens=True)
            note = full_text.split("[/INST]")[-1].strip() if "[/INST]" in full_text else full_text[len(prompt):].strip()
            if len(note) > 50:
                all_notes.append({"text": note, "specialty": specialty, "synth_id": f"synth_{len(all_notes):04d}"})

    df = pd.DataFrame(all_notes)
    df.to_csv(OUTPUT_DIR / "synthetic_notes.csv", index=False)
    del model, tokenizer
    torch.cuda.empty_cache()
    print(f"  Generated {len(df)} notes")
    return df.head(n)


